            <h2 id="section_2">Covenant Special Abilities</h2>
            <p>All members of the Kyrian Covenant receive one signature ability and one class combat ability. The signature ability is the same for all Kyrian, while the combat ability is different for each class.
            </p>
            <h3 id="section_2_1">Signature Ability</h3>
            <p> Summon Steward Call your steward to bring you a Phial of Serenity that can be consumed to restore 20% health and remove all Curse, Disease, Poison, and Bleed effects. Your Steward additionally offers access to a selection of useful amenities
                for 4 min.
            </p>
            <p>If you have the level 2 Kyrian Transport system unlocked, your Steward can also summon an Elysian Beacon, giving you access to your Transport Network from anywhere within Bastion once every 8 hours. At level 3, the Beacon becomes available
                to all party members
            </p>
            <h3 id="section_2_2">Class Combat Abilities</h3>
            <p>These are different for each class.</p>
            <h4 id="section_2_2_1">Death Knight</h4>
            <p> Shackle the Unworthy is a hasted single target damage over time cooldown ability. While Shackle the Unworthy is active on an enemy, that enemy deals 5% less damage to you and your direct damage rune-spending attacks have a 50% to spread the
                DoT at its remaining duration to another nearby enemy that does not already have Shackle the Unworthy.</p>
            <h4 id="section_2_2_2">Demon Hunter</h4>
            <p> Elysian Decree is ground-targeted sigil that detonates to deal AoE damage to enemies within the sigil and shatters 3 Lesser Soul Fragments.</p>
            <h4 id="section_2_2_3">Druid</h4>
            <p> Kindred Spirits is a two-part ability. After a short cast you'll form a bond with another ally that lasts for an hour. The bond itself grants no inherent power, but can be empowered every minute for 10s to grant you and your partner an effect
                based on your role and your partner's role.</p>
            <h4 id="section_2_2_4">Hunter</h4>
            <p> Resonating Arrow is a ground-targeted cooldown ability that marks an area dealing damage to all enemies in the area. Enemies within the area can be attacked by you and your pet regardless of being within line of sight, and your attacks and
                your pets attacks on enemies within the area have 30% greater critical strike chance.</p>
            <h4 id="section_2_2_5">Mage</h4>
            <p> Radiant Spark is a short cooldown ability that inflicts some upfront damage and then applies a short duration damage over time debuff. Targets affected by Radiant Spark take 10% increased damage from your direct damage spells stacking for
                each spell. Radiant Spark immediately ends after 4 spell casts.</p>
            <h4 id="section_2_2_6">Monk</h4>
            <p> Weapons of Order is a cooldown ability that significantly increases your mastery for a moderate duration. Additionally, each specialization gains an additional benefit.</p>
            <h4 id="section_2_2_7">Paladin</h4>
            <p> Divine Toll is a cooldown ability that unleashes 5 replications of a primary ability to allies or enemies within 30 yards.</p>
            <h4 id="section_2_2_8">Priest</h4>
            <p> Boon of the Ascended is a long cooldown ability that when activated is replaced by the short cooldown ability Ascended Nova for 10s that deals AoE damage and healing to 6 nearby enemies and allies as well as granting 1 stack of the Boon of
                the Ascended buff. Additionally, a core damaging ability is transformed into Ascended Blast causing damage when cast and healing a nearby ally as well as granting 5 stacks of the Boon of the Ascended buff. Once the 10s duration has expired,
                you explode for AoE damage and healing via Ascended Eruption to nearby allies increased by 3% for each stack of Boon of the Ascended that you've built up.</p>
            <h4 id="section_2_2_9">Rogue</h4>
            <p> Echoing Reprimand is a short cooldown ability that deals upfront damage to generate 2 combo points and overcharge one of your combo points for 45s; typically, the 2nd, 3rd or 4th combo point indicated by the in-game combo point resource display
                or the stack number on the corresponding buff. For the remaining duration of the buff, any combo point spending ability that consumes combo points exactly at the overcharge threshold will consume as if 7 combo points were consumed.</p>
            <h4 id="section_2_2_10">Shaman</h4>
            <p> Vesper Totem is a ground-targeted cooldown ability that places a totem at the designated area. When you cast a damage ability the totem radiates AoE damage to 6 enemies near the totem, and similarly when you cast a healing ability the totem
                radiates AoE healing to 6 allies near the totem. The totem can be relocated to a new designated area simply by reactivating the ability while the totem is active.</p>
            <h4 id="section_2_2_11">Warlock</h4>
            <p> Scouring Tithe is a cooldown ability that deals upfront damage and applies an 18s damage over time effect to your main target. If the target dies with the DoT still applied, then you'll generate 5 Soul Shards. If the DoT expires naturally
                after 18s then the cooldown is instantly refreshed.</p>
            <h4 id="section_2_2_12">Warrior</h4>
            <p> Spear of Bastion is a ground-targeted AoE cooldown ability that generates 25 rage, deals upfront damage, and applies a short duration damage over time effect to any enemies in the targeted location. All enemies hit are tethered to the targeted
                location for the duration of the DoT.</p>
